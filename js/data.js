/* ================================================================
   2. 异步题库加载
   ================================================================ */

let _fastQuestions = null;
let _standardQuestions = null;

export async function loadQuestions(mode) {
  if (mode === 'fast' && _fastQuestions) return _fastQuestions;
  if (mode === 'standard' && _standardQuestions) return _standardQuestions;

  const url = mode === 'fast'
    ? new URL('../assets/questions-fast.json', import.meta.url).href
    : new URL('../assets/questions-standard.json', import.meta.url).href;

  try {
    const resp = await fetch(url);
    const data = await resp.json();
    if (mode === 'fast') { _fastQuestions = data; }
    else { _standardQuestions = data; }
    return data;
  } catch (e) {
    console.error('[data] 加载题库失败:', e);
    return null;
  }
}

